namespace Morpher.Russian
{
    public class RussianInitials : IParadigm
    {
        public string Nominative    {get { return "И"; }}
        public string Genitive      {get { return "Р"; }}
        public string Dative        {get { return "Д"; }}
        public string Accusative    {get { return "В"; }}
        public string Instrumental  {get { return "Т"; }}
        public string Prepositional {get { return "П"; }}
        public string Locative      {get { return "М"; }}
    }
}